"""
Python = Linguagem de programação

Tipo de tipagem = Dinâmica / Forte
Tipagem Dinâmica = Não é preciso definir a tipagem do dado, o compilador vai saber qual tipo de dado você está passando
Tipagem Forte = Não permite que um dado de um tipo seja tratado como outro tipo, por exemplo, um uppercase em uma variavel int, ou uma soma entre um numero e uma string

str -> string -> texto
String são textos dentro de aspas
"""

# Aspas simples
print('Márcio')
print(1, '"Márcio"')

# Aspas duplas
print("Márcio")
print(2, "'Márcio'")

# Escape - Serve para utilizar alguns caracteres no texto
print("Márcio \"Paulin\" ")

# r - Utilizado para regex
print(r"Márcio \"Paulin \"")
