# Tipos int e float
# int -> Inteiro
# int representa qualquer número, positivo ou negativo.
# int sem sinal é considerado positivo.
print(11) # int positivo
print(-11) # int negativo


# float -> Ponto flutuante
# O tipo float representa qualquer número positivo ou negativo,
# Mas com um ponto flutuante.
# float sem sinal é positivo, assim como int.
print(1.1) # float positivo
print(-1.1) # float negativo


# A função type mostra o tipo que foi inferido ao valor
print(type("Márcio"))
print(type(11))
print(type(1.0), type(-1.0))